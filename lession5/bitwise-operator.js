// đây là toán tử để chúng ta làm việc với số bit  



let x = 3 
let y = 5 

console.log(x & y)   // AND    : 0001 và 1 
console.log(x | y)   // OR  : 0111 (7)
console.log(x ^ y)   // XOR :  0110 (6)
console.log(x / y)   // NOI : 1010 ()
console.log(x << y)  // dịch sang bên trái 
console.log(x >> y)  // dịch sang bên phải  

