let day = new Date().getDay() 

switch(day) {
    case 0 :    
    console.log('monday')
    case 1 :
    console.log('tuesday')
    case 2 : 
    console.log('wednesday')
    case 3  : 
    console.log('thursday')
    case 4 :
        console.log('friday')
    case 5 :
        console.log('Saturday')
    case 6 :
        console.log('Sunday')
    default :
    console.log('invalid day')

}